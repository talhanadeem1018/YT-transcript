import { getUserFromRequest } from './_lib/auth';
import {
  buildAccountSummary,
  consumeCreditIfNeeded,
  ensureProfile,
  hasUnlimitedAccessForProfile,
  refundCredit,
} from './_lib/account';
import { requireEnv } from './_lib/env';
import { supabaseAdmin } from './_lib/supabaseAdmin';

const APIFY_TOKEN = requireEnv('APIFY_API_TOKEN');
const APIFY_ACTOR_ID = process.env.APIFY_ACTOR_ID || 'prodiger/youtube-transcript-scraper---transcriber';

function json(res, status, body) {
  res.status(status).setHeader('Content-Type', 'application/json');
  res.end(JSON.stringify(body));
}

async function fetchApifyTranscript(videoUrl) {
  const endpoint = `https://api.apify.com/v2/acts/${encodeURIComponent(
    APIFY_ACTOR_ID,
  )}/run-sync-get-dataset-items?token=${encodeURIComponent(APIFY_TOKEN)}`;

  const response = await fetch(endpoint, {
    method: 'POST',
    headers: { 'Content-Type': 'application/json' },
    body: JSON.stringify({
      format: 'text',
      includeTimestamps: false,
      urls: [videoUrl],
      includeAutoGenerated: true,
      proxyConfiguration: { useApifyProxy: true, apifyProxyGroups: ['RESIDENTIAL'] },
    }),
  });

  if (!response.ok) {
    const fallbackText = await response.text();
    throw new Error(`Apify request failed: ${fallbackText.slice(0, 180)}`);
  }

  const items = await response.json();
  const item = items?.[0];

  if (!item) {
    throw new Error('No transcript was returned for this video.');
  }

  if (item.error) {
    throw new Error(item.error);
  }

  const transcript = item.transcript || item.text || item.plainText;
  if (!transcript) {
    throw new Error('Transcript text was missing in the actor response.');
  }

  return {
    transcript,
    videoId: item.videoId || item.video_id,
    videoUrl: item.videoUrl || videoUrl,
    videoTitle: item.title || item.video_title || 'Untitled video',
    language: item.language || 'unknown',
    transcriptMethod: item.transcriptMethod || item.method || 'captions',
  };
}

export default async function handler(req, res) {
  if (req.method !== 'POST') {
    return json(res, 405, { error: 'Method not allowed.' });
  }

  try {
    const user = await getUserFromRequest(req);
    const startingProfile = await ensureProfile(user);

    const { videoInput } = req.body || {};
    if (!videoInput) {
      return json(res, 400, { error: 'videoInput is required.' });
    }

    const transcriptData = await fetchApifyTranscript(videoInput);
    let profile = await consumeCreditIfNeeded(startingProfile);
    const usedCredit = !hasUnlimitedAccessForProfile(startingProfile);

    const insertPayload = {
      user_id: user.id,
      video_id: transcriptData.videoId,
      video_url: transcriptData.videoUrl,
      video_title: transcriptData.videoTitle,
      transcript: transcriptData.transcript,
      language: transcriptData.language,
      transcript_method: transcriptData.transcriptMethod,
    };

    const { data: savedRecord, error: saveError } = await supabaseAdmin
      .from('transcripts')
      .insert(insertPayload)
      .select()
      .single();

    if (saveError) {
      if (usedCredit) {
        profile = await refundCredit(profile);
      }
      throw saveError;
    }

    return json(res, 200, {
      transcript: savedRecord.transcript,
      meta: {
        title: savedRecord.video_title,
        language: savedRecord.language,
        method: savedRecord.transcript_method,
      },
      account: buildAccountSummary(profile),
      message: 'Transcript generated and saved to history.',
    });
  } catch (error) {
    return json(res, 400, { error: error.message || 'Unexpected server error.' });
  }
}
